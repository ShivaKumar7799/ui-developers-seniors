// Increment and Decrement Operators 


// example for post increment 

var  a = 10;
var b = a++;
console.log(a, "value of a, post increment")
console.log(b, "value of b, post increment")

// example for pre increment

var c = 10;
var d= ++c;
console.log(c, "value of c, pre-increment")
console.log(d, "value of d, pre increment")

// example for post decrement 

var  e = 10;
var f = e--;
console.log(e, "value of e, post decrement")
console.log(f, "value of f, post decrement")

// example for pre decrement

var g = 10;
var h= --g;
console.log(g, "value of g, pre-decrement")
console.log(h, "value of h, pre decrement")

